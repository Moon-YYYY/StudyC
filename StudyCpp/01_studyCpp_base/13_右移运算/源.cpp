#include<iostream>
using namespace std;
//右移运算符 >>
int main() {
	//1、正数的右移
	// x >> y;相当于x ÷ 2的y次幂
	int a = 0b111;
	a = (a >> 1);
	//2、负数的右移
	int b = -1;
	cout << (-1 >> 1) << endl;
	//11111111 11111111 11111111 11111111 = -1
	//11111111 11111111 11111111 11111111 = -1  // -1右移一位还是他本身

	//3、去掉低k位
	int c = 0b10000101;
	cout << (c >> 7) << endl;//右移了7位，被移没的就消失了 实现去掉低k位

	//4、取到第低k位的值
	int d = 0b101010101;
	cout << ((d >> 4) & 1) << endl;//右移四位 和1进行位与运算 如果是0则取到0否则为1

	return 0;
}